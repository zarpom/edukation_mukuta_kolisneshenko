package lesson3;

public class FloatVariable {
    public static void main(String[] args) {
        float f1 = (float) 1.5;

        float f2 = 1.5F;

        System.out.println(f1 + " " + f2);
        System.out.println("Це double по дефолту:" + 14.88);

        double d1 = 0.44;
        double d2 = 1.34d;
    }
}
